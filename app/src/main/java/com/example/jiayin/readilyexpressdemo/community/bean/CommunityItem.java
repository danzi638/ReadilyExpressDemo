package com.example.jiayin.readilyexpressdemo.community.bean;

public class CommunityItem {

    private String name;

    private int imageId;

    public CommunityItem(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
